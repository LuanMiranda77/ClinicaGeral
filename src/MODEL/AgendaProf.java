package MODEL;


public class AgendaProf {
	
	private int id;
	private String nome;
	private Dia domingo;
	private Dia segunda;
	private Dia terca;
	private Dia quarta;
	private Dia quinta;
	private Dia sexta;
	private Dia sabado;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Dia getDomingo() {
		return domingo;
	}
	public void setDomingo(Dia domingo) {
		this.domingo = domingo;
	}
	public Dia getSegunda() {
		return segunda;
	}
	public void setSegunda(Dia segunda) {
		this.segunda = segunda;
	}
	public Dia getTerca() {
		return terca;
	}
	public void setTerca(Dia terca) {
		this.terca = terca;
	}
	public Dia getQuarta() {
		return quarta;
	}
	public void setQuarta(Dia quarta) {
		this.quarta = quarta;
	}
	public Dia getQuinta() {
		return quinta;
	}
	public void setQuinta(Dia quinta) {
		this.quinta = quinta;
	}
	public Dia getSexta() {
		return sexta;
	}
	public void setSexta(Dia sexta) {
		this.sexta = sexta;
	}
	public Dia getSabado() {
		return sabado;
	}
	public void setSabado(Dia sabado) {
		this.sabado = sabado;
	}
	
	
	


}
